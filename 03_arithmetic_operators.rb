# Math: Arithmetic Operators

=begin
  + addition
  - subtraction
  * multiplication
  / division
  % modulo (remainder)
  ** exponential (power)
=end
puts "\n\n\n"

a = 7;
b = 2;

puts a + b
# Output: 9

puts a - b
# Output: 5

puts a * b
# Output: 14

puts a % b
# Output: 1

puts a ** b
# Output: 49

puts a / b
# Output: 3

c = 9.0
d = 4

puts c / d
# Output: 2.25
